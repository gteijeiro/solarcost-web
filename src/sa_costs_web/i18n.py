def normalize_language(value: Any) -> str:
    language = str(value or "").strip().lower()
    if language in SUPPORTED_LANGUAGES:
        return language
    return DEFAULT_LANGUAGE


def translate(language: str, text: str, **kwargs: Any) -> str:
    normalized = normalize_language(language)
    template = TRANSLATIONS.get(normalized, {}).get(text, text)
    if kwargs:
        return template.format(**kwargs)
    return template


def get_locale(language: str) -> str:
    return LOCALE_BY_LANGUAGE.get(normalize_language(language), LOCALE_BY_LANGUAGE[DEFAULT_LANGUAGE])


def get_month_abbr(language: str) -> tuple[str, ...]:
    return MONTH_ABBR.get(normalize_language(language), MONTH_ABBR[DEFAULT_LANGUAGE])


def get_language_options() -> list[dict[str, str]]:
    return [{"code": code, "label": LANGUAGE_LABELS[code]} for code in SUPPORTED_LANGUAGES]
